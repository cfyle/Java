
public class CircleHasPoint {
	private int raidus;
	private Point2D center;
	public CircleHasPoint(int raidus, Point2D center) {
		super();
		this.raidus = raidus;
		this.center = center;
	}
	public int getRaidus() {
		return raidus;
	}
	public void setRaidus(int raidus) {
		this.raidus = raidus;
	}
	public Point2D getCenter() {
		return center;
	}
	public void setCenter(Point2D center) {
		this.center = center;
	}
	
	
}
